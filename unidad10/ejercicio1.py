# TODAS LAS LETRAS EN EL STRING SE IMPRIMIRAN EN MAYUSCULA
v = "Hola Mundo".upper()
print(v)
# LA PRIMER LETRA SE IMPRIMIRA EN MAYUSCULA, LAS DEMAS EN MINUSCULAS
b = "hola mundo".capitalize()
print(b)
# TODAS LAS LETRAS EN EL STRING SE IMPRIMIRAN EN MINUSCULA
c = "HOLA MUNDO".lower()
print(c)
# count muestra cuantas veces se ha imprimido la letra o numero a buscar
d = "Hola Mundo".count("z")
print(d)
e = "Hola Mundo,otra,palabra".split(",")
print(e)
f = "hola mundo".replace("mundo", "marte")
print(f)
g = " hola mundo mundo mundo mundo".replace("mundo", "", 3)
print(g)

